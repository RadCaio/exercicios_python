valor1 = int(input('digite um valor'))
valor2 = int(input('digite outro valor'))
soma = valor1 + valor2
sub = valor1 - valor2
mult = valor1 * valor2
div_inter = valor1 // valor2
potencia = valor1 ** valor2
print('a soma vai ser: {} \n a subtração vai ser: {} \n a multiplicação será: {} \n'.format(soma,sub,mult),end='')
print('a divisão inteira será: {} \n a potencia será: {}'.format(div_inter,potencia))